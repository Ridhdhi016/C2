/*Program to find out the range(min and max) of the given data types.*/
public class DataTypeMinMaxValue {

	public static void main(String[] args) {
		System.out.println("Byte\n" + "Min Value:" + Byte.MIN_VALUE + "\nMax Value:" + Byte.MAX_VALUE);
		System.out.println("\nShort\n" + "Min Value:" + Short.MIN_VALUE + "\nMax Value:" + Short.MAX_VALUE);
		System.out.println("\nInteger\n" + "Min Value:" + Integer.MIN_VALUE + "\nMax Value:" + Integer.MAX_VALUE);
		System.out.println("\nLong\n" + "Min Value:" + Long.MIN_VALUE + "\nMax Value:" + Long.MAX_VALUE);
		System.out.println("\nFloat\n" + "Min Value:" + Float.MIN_VALUE + "\nMax Value:" + Float.MAX_VALUE);
		System.out.println("\nDouble\n" + "Min Value:" + Double.MIN_VALUE + "\nMax Value:" + Double.MAX_VALUE);

	}

}
