/*Program to find the default value of the given data types.*/
public class DataTypeDefaultValue {

	static boolean b;
	static char c;
	static byte bt;
	static short s;
	static int i;
	static long l;
	static float f;
	static double d;
	public static void main(String[] args) {
		System.out.println("Default value of Boolean: " + b);
		System.out.println("Default value of Character: " + c);
		System.out.println("Default value of Byte: " + bt);
		System.out.println("Default value of Short: " + s);
		System.out.println("Default value of Integer: " + i);
		System.out.println("Default value of Long: " + l);
		System.out.println("Default value of Float: " + f);
		System.out.println("Default value of Double: " + d);
	}
}
